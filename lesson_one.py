# Арифметические операции
# Сложение двух чисел: 
print(6 + 2) # 8
# Вычетание двух чисел:
print(6 - 2) # 4
# Умножение двух чисел:
print(6 * 2) # 12
# Деление двух чисел:
print(6 / 2) # 3.0
# Целочисленное деление двух чисел:
print(7 / 2) # 3.5
print(7 // 2) # 3
# Возвдение в степень:
print(6 ** 2) # 36
# Взятие остатка от деления:
print(7 % 2) # Результат - 1
